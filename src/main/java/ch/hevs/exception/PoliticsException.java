package ch.hevs.exception;

public class PoliticsException extends RuntimeException {

	public PoliticsException() {
		super();
	}

	public PoliticsException(String arg0) {
		super(arg0);
	}

	public PoliticsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PoliticsException(Throwable arg0) {
		super(arg0);
	}

}
